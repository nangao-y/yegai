"""
    len()  :  字符串的长度
    format:格式化输出(进阶)
    
     1.格式化小数长度(四舍五入)    :.2f
     2.将小数以百分比的形式表示    :.2%
"""
s1 = 5.8723741
print("今天雅思考了{:.2f}分".format(s1))
s2 = 0.82734
print("百分比为{1:.3%}".format(s2,s1))
