"""
    格式化输出方法%
%s:为字符占位
%d:为数值类型占位
%f:为浮点数占位
s1 = "%s------%d--------%f-------"%('a','b','c')

    F表达式
"""
a1 = "我%s你"%("爱爱爱")
print(a1)
a2 = "我的考试成绩是%d"%(89)
print(a2)
a3 = "隔壁的数字五分裂成了%f"%(2.5)
print(a3)
a4 = "集合为%s"%([1,2])
print(a4)
print("==============分割线==============")
